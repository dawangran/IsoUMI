#!/usr/bin/env python3
"""Summarize IsoUMI benchmark output reports into a manuscript-ready TSV.

Usage:
  python3 scripts/summarize_benchmark_reports.py benchmarks/output

The script expects benchmark outputs generated by scripts/benchmark_isoumi.sh:

  <out-dir>/metrics.tsv
  <out-dir>/<label>.molecules.tsv
  <out-dir>/<label>.assignments.tsv
  <out-dir>/<label>.corrections.tsv

It writes <out-dir>/summary.tsv by default.
"""

from __future__ import annotations

import argparse
import csv
import math
from pathlib import Path


SUMMARY_COLUMNS = [
    "dataset",
    "label",
    "version",
    "input_path",
    "input_format",
    "input_size_bytes",
    "input_reads",
    "input_cell_barcodes",
    "input_umis",
    "assigned_reads",
    "assigned_cell_barcodes",
    "molecules",
    "molecule_reads",
    "duplicates",
    "duplicate_rate",
    "corrected_umis",
    "self_umis",
    "correction_rate",
    "mean_merge_confidence",
    "elapsed_seconds",
    "peak_rss_kb",
    "peak_rss_mb",
    "threads",
    "buckets",
    "mode_params",
    "command",
]


def read_metrics(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        raise FileNotFoundError(f"missing metrics file: {path}")
    with path.open("r", encoding="utf-8", newline="") as handle:
        rows = list(csv.DictReader(handle, delimiter="\t"))
    if not rows:
        raise ValueError(f"metrics file has no data rows: {path}")
    return rows


def require_report(path: Path, allow_missing: bool) -> bool:
    if not path.exists():
        if allow_missing:
            return False
        raise FileNotFoundError(f"missing benchmark report: {path}")
    return True


def parse_int(value: str) -> int:
    try:
        return int(value)
    except (TypeError, ValueError):
        return 0


def summarize_molecules(path: Path, allow_missing: bool) -> tuple[int, int]:
    if not require_report(path, allow_missing):
        return 0, 0
    molecules = 0
    molecule_reads = 0
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle, delimiter="\t")
        for row in reader:
            molecules += 1
            molecule_reads += parse_int(row.get("count", "0"))
    return molecules, molecule_reads


def summarize_assignments(path: Path, allow_missing: bool) -> tuple[int, int, int]:
    if not require_report(path, allow_missing):
        return 0, 0, 0
    assigned = 0
    duplicates = 0
    cells: set[str] = set()
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle, delimiter="\t")
        for row in reader:
            assigned += 1
            if row.get("dup") == "1":
                duplicates += 1
            molecule_id = row.get("molecule_id", "")
            if molecule_id:
                cell = molecule_id.split("|", 1)[0]
                if cell and cell != "NA":
                    cells.add(cell)
    return assigned, duplicates, len(cells)


def summarize_corrections(path: Path, allow_missing: bool) -> tuple[int, int, float]:
    if not require_report(path, allow_missing):
        return 0, 0, 0.0
    corrected = 0
    self_rows = 0
    conf_sum = 0.0
    conf_n = 0
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle, delimiter="\t")
        for row in reader:
            raw = row.get("raw_umi", "")
            corr = row.get("corr_umi", "")
            if raw == corr:
                self_rows += 1
            else:
                corrected += 1
                try:
                    conf = float(row.get("confidence", "nan"))
                except ValueError:
                    continue
                if math.isfinite(conf):
                    conf_sum += conf
                    conf_n += 1
    mean_conf = conf_sum / conf_n if conf_n else 0.0
    return corrected, self_rows, mean_conf


def format_rate(numerator: int, denominator: int) -> str:
    if denominator <= 0:
        return "0.0000"
    return f"{numerator / denominator:.4f}"


def metric_value(metric: dict[str, str], key: str, default: str = "NA") -> str:
    value = metric.get(key, "")
    return value if value != "" else default


def summarize(out_dir: Path, allow_missing: bool = False) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    for metric in read_metrics(out_dir / "metrics.tsv"):
        label = metric["label"]
        molecules, molecule_reads = summarize_molecules(
            out_dir / f"{label}.molecules.tsv", allow_missing
        )
        assigned, duplicates, assigned_cells = summarize_assignments(
            out_dir / f"{label}.assignments.tsv", allow_missing
        )
        corrected, self_rows, mean_conf = summarize_corrections(
            out_dir / f"{label}.corrections.tsv", allow_missing
        )
        correction_total = corrected + self_rows
        rows.append(
            {
                "dataset": metric_value(metric, "dataset"),
                "label": label,
                "version": metric_value(metric, "version"),
                "input_path": metric_value(metric, "input_path"),
                "input_format": metric_value(metric, "input_format"),
                "input_size_bytes": metric_value(metric, "input_size_bytes"),
                "input_reads": metric_value(metric, "input_reads"),
                "input_cell_barcodes": metric_value(metric, "input_cell_barcodes"),
                "input_umis": metric_value(metric, "input_umis"),
                "assigned_reads": str(assigned),
                "assigned_cell_barcodes": str(assigned_cells),
                "molecules": str(molecules),
                "molecule_reads": str(molecule_reads),
                "duplicates": str(duplicates),
                "duplicate_rate": format_rate(duplicates, assigned),
                "corrected_umis": str(corrected),
                "self_umis": str(self_rows),
                "correction_rate": format_rate(corrected, correction_total),
                "mean_merge_confidence": f"{mean_conf:.4f}",
                "elapsed_seconds": metric_value(metric, "elapsed_seconds"),
                "peak_rss_kb": metric_value(metric, "peak_rss_kb"),
                "peak_rss_mb": metric_value(metric, "peak_rss_mb"),
                "threads": metric_value(metric, "threads"),
                "buckets": metric_value(metric, "buckets"),
                "mode_params": metric_value(metric, "mode_params", ""),
                "command": metric_value(metric, "command", ""),
            }
        )
    return rows


def write_summary(path: Path, rows: list[dict[str, str]]) -> None:
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, delimiter="\t", fieldnames=SUMMARY_COLUMNS)
        writer.writeheader()
        writer.writerows(rows)


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Summarize IsoUMI benchmark reports into a compact TSV table."
    )
    parser.add_argument("out_dir", help="Benchmark output directory.")
    parser.add_argument(
        "--out",
        help="Output TSV path (default: <out-dir>/summary.tsv).",
    )
    parser.add_argument(
        "--allow-missing",
        action="store_true",
        help="Write zero-valued summaries for missing report files instead of failing.",
    )
    args = parser.parse_args()

    out_dir = Path(args.out_dir)
    out_path = Path(args.out) if args.out else out_dir / "summary.tsv"
    rows = summarize(out_dir, allow_missing=args.allow_missing)
    write_summary(out_path, rows)
    print(f"Wrote summary: {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
